package com.example.benchmark.entity;

public class BenchMarkDetails {
    String question;
    String esgType;
    String esgIndicators;
    String primaryDetails;
    String secondaryDetails;
    String citationDetails;
    String pageNumber;

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getEsgType() {
        return esgType;
    }

    public void setEsgType(String esgType) {
        this.esgType = esgType;
    }

    public String getEsgIndicators() {
        return esgIndicators;
    }

    public void setEsgIndicators(String esgIndicators) {
        this.esgIndicators = esgIndicators;
    }

    public String getPrimaryDetails() {
        return primaryDetails;
    }

    public void setPrimaryDetails(String primaryDetails) {
        this.primaryDetails = primaryDetails;
    }

    public String getSecondarDetails() {
        return secondaryDetails;
    }

    public void setSecondarDetails(String secondarDetails) {
        this.secondaryDetails = secondarDetails;
    }

    public String getCitationDetails() {
        return citationDetails;
    }

    public void setCitationDetails(String citationDetails) {
        this.citationDetails = citationDetails;
    }

    public String getPageNumber() {
        return pageNumber;
    }

    public void setPageNumber(String pageNumber) {
        this.pageNumber = pageNumber;
    }
}
